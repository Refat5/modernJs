// switch statements
// 

const grade = '65';

switch(grade){
	case '90':
	console.log('you got A+');
	break;

	case '70':
	console.log('you got A');
	break;

	case '65':
	console.log('you got A-');
	break;

	case '50':
	console.log('you got B');
	break;

	case '40':
	console.log('you got c');
	break;

	default:
	console.log('you are fail');
}